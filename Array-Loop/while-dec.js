
document.write("<h3>while Loop</h3>")

//Increment
document.write(" <b>Increment</b> <br>")
a = 1
while(a<=10){
    document.write( a + "<br>")
    a++
}


document.write("<br> <br>")

// Decrement
document.write(" <b>Decrement</b> <br>")
i = 10
while (i>=1) {
    document.write( i + "<br>")
    i--
}