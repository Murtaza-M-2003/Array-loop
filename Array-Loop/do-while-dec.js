
document.write("<h3>do-while Loop</h3>")

//Increment
document.write(" <b>Increment</b> <br>")
a = 1
do {
    document.write( a + "<br>")
    a++
} while (a<=10);


document.write("<br> <br>")

// Decrement
document.write(" <b>Decrement</b> <br>")

i = 10
do {
    document.write( i + "<br>")
    i--
} while (i>=1);