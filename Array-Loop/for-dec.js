
document.write("<h3>for Loop</h3>")

//Increment
document.write(" <b>Increment</b> <br>")
for(a=1; a<=10; a++){
    document.write( a + "<br>")
}


document.write("<br> <br>")

// Decrement
document.write(" <b>Decrement</b> <br>")
for(i=10; i>=1; i--){
    document.write(i + "<br>")
}